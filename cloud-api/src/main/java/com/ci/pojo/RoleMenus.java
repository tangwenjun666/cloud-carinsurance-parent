package com.ci.pojo;

public class RoleMenus {
	private int rid;// 角色序号
	private int mid;// 菜单序号
	private int bid;// 按钮权限

	public int getRid() {
		return rid;
	}

	public void setRid(int rid) {
		this.rid = rid;
	}

	public int getMid() {
		return mid;
	}

	public void setMid(int mid) {
		this.mid = mid;
	}

	public int getBid() {
		return bid;
	}

	public void setBid(int bid) {
		this.bid = bid;
	}

}
